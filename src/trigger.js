/**
 * easycron Trigger Generator
 * Generates external trigger configurations:
 *   - GitHub Actions YAML with smart retry
 *   - UptimeRobot config (text output)
 *   - cron-job.org config (text output)
 *   - Keep-awake helper configs
 *
 * Phase 2 additions:
 *   - cron-job.org provider support
 *   - Full redundancy mode (all 3 providers)
 *   - Configurable retry count and delay
 *   - HTTP method + body support for POST triggers
 *   - Keep-awake /health endpoint scaffolding
 *
 * Handles:
 *   - Auth header injection
 *   - YAML overwrite collision safety
 *   - Smart 3x retry with 10s backoff (configurable)
 *   - Differentiation between timeout and 5xx errors
 */

'use strict';

const fs = require('fs');
const path = require('path');

// ─── GitHub Actions Generator ────────────────────────────────────

/**
 * Generate a GitHub Actions workflow YAML file.
 * @param {{ url: string, cron: string, auth: string|null, outputDir: string, retries: number, delay: number, method: string, body: string|null }} options
 * @returns {string} Path to the generated file
 */
function generateGitHubAction({ url, cron, auth, outputDir, retries = 3, delay = 10, method = 'GET', body = null }) {
  const workflowDir = path.join(outputDir, '.github', 'workflows');

  // Ensure directory exists
  fs.mkdirSync(workflowDir, { recursive: true });

  // Collision safety: if file exists, increment the name
  let filename = 'easycron-trigger.yml';
  let filePath = path.join(workflowDir, filename);
  let counter = 2;

  while (fs.existsSync(filePath)) {
    filename = `easycron-trigger-${counter}.yml`;
    filePath = path.join(workflowDir, filename);
    counter++;
  }

  // Build curl command with optional auth, method, and body
  let curlCmd = 'curl -s -L -o /dev/null -w "%{http_code}" -m $TIMEOUT';
  if (method === 'POST') {
    curlCmd += ' -X POST';
    curlCmd += ' -H "Content-Type: application/json"';
    if (body) {
      curlCmd += " -d '" + body + "'";
    }
  }
  if (auth) {
    curlCmd += ' -H "Authorization: ' + auth + '"';
  }
  curlCmd += ' "' + url + '"';

  // YAML content with hardened retry logic
  // NOTE: We use string concatenation to avoid JS eating bash $VARIABLE references
  //
  // Robustness features:
  //   - curl -L follows redirects (prevents 3xx false-failures)
  //   - 30s timeout (free-tier cold starts can take 20-25s)
  //   - 2xx AND 3xx treated as success
  //   - 5xx retried (cold-start often causes transient 502/503)
  //   - 4xx fails immediately (misconfigured URL/auth)
  //   - Timeout/connection-refused retried (server booting)
  const yaml = [
    '# Generated by easycron CLI — https://github.com/namanjain24-sudo/easycron',
    '# Schedule: ' + cron,
    '# Target: ' + url,
    '# Method: ' + method,
    '#',
    '# ⚠️ GitHub Actions schedules use UTC time.',
    '# ⚠️ Intervals < 5 min may exhaust free tier quickly (2000 min/month).',
    '#',
    '# 💡 Retry config: MAX_RETRIES=' + retries + ', RETRY_DELAY=' + delay + 's',
    '#    Edit these values below if your server needs more boot time.',
    '',
    'name: easycron-trigger',
    'on:',
    '  schedule:',
    '    - cron: "' + cron + '"',
    '  workflow_dispatch: {} # Allow manual trigger for testing',
    '',
    'jobs:',
    '  trigger:',
    '    runs-on: ubuntu-latest',
    '    steps:',
    '      - name: Hit Endpoint with Smart Retry',
    '        run: |',
    '          MAX_RETRIES=' + retries,
    '          RETRY_DELAY=' + delay,
    '          TIMEOUT=30',
    '          ',
    '          for i in $(seq 1 $MAX_RETRIES); do',
    '            echo "Attempt $i/$MAX_RETRIES..."',
    '            ',
    '            HTTP_CODE=$(' + curlCmd + ') || true',
    '            ',
    '            # Success (2xx or 3xx — redirects are normal)',
    '            if [ "$HTTP_CODE" -ge 200 ] 2>/dev/null && [ "$HTTP_CODE" -lt 400 ] 2>/dev/null; then',
    '              echo "✔ Success (HTTP $HTTP_CODE)"',
    '              exit 0',
    '            fi',
    '            ',
    '            # 4xx = client error, fail immediately (bad URL/auth — retrying won\'t help)',
    '            if [ "$HTTP_CODE" -ge 400 ] 2>/dev/null && [ "$HTTP_CODE" -lt 500 ] 2>/dev/null; then',
    '              echo "✖ Client error (HTTP $HTTP_CODE) — check URL/auth"',
    '              exit 1',
    '            fi',
    '            ',
    '            # 5xx or timeout = server likely cold-starting, retry',
    '            if [ $i -lt $MAX_RETRIES ]; then',
    '              echo "⏳ Server not ready (HTTP $HTTP_CODE), retrying in ${RETRY_DELAY}s..."',
    '              sleep $RETRY_DELAY',
    '            fi',
    '          done',
    '          ',
    '          echo "✖ All $MAX_RETRIES attempts failed"',
    '          exit 1',
    '',
  ].join('\n');

  fs.writeFileSync(filePath, yaml, 'utf-8');
  return filePath;
}

// ─── UptimeRobot Config Generator ────────────────────────────────

/**
 * Generate an UptimeRobot configuration string.
 * @param {{ url: string, intervalMinutes: number, auth: string|null }} options
 * @returns {string} Configuration instructions
 */
function generateUptimeRobotConfig({ url, intervalMinutes, auth }) {
  const interval = Math.max(intervalMinutes, 5); // UptimeRobot minimum is 5 min on free tier

  const lines = [
    '',
    '  ┌──────────────────────────────────────────────────────┐',
    '  │            UptimeRobot Configuration                 │',
    '  ├──────────────────────────────────────────────────────┤',
    '  │  Monitor Type:    HTTP(s)                            │',
    '  │  URL:             ' + url.substring(0, 35).padEnd(35) + '│',
    '  │  Interval:        Every ' + (interval + ' min').padEnd(29) + '│',
    '  │  Monitoring Type: Keyword                            │',
    '  │  Keyword:         OK                                 │',
  ];

  if (auth) {
    lines.push('  │  HTTP Headers:                                       │');
    lines.push('  │    Authorization: ' + auth.substring(0, 35).padEnd(35) + '│');
  }

  lines.push('  ├──────────────────────────────────────────────────────┤');
  lines.push('  │  Setup Steps:                                        │');
  lines.push('  │  1. Go to https://uptimerobot.com/dashboard          │');
  lines.push('  │  2. Click "+ Add New Monitor"                        │');
  lines.push('  │  3. Enter the values above                           │');
  lines.push('  │  4. Save — done!                                     │');
  lines.push('  └──────────────────────────────────────────────────────┘');
  lines.push('');

  return lines.join('\n');
}

