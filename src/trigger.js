/**
 * Generate a cron-job.org configuration string.
 * @param {{ url: string, cron: string, auth: string|null }} options
 * @returns {string} Configuration instructions
 */
function generateCronJobOrgConfig({ url, cron, auth }) {
  const lines = [
    '',
    '  ┌──────────────────────────────────────────────────────┐',
    '  │           cron-job.org Configuration                 │',
    '  ├──────────────────────────────────────────────────────┤',
    '  │  URL:             ' + url.substring(0, 35).padEnd(35) + '│',
    '  │  Schedule (cron): ' + cron.padEnd(35) + '│',
    '  │  Timezone:        UTC                                │',
    '  │  Request Method:  GET                                │',
    '  │  Timeout:         30 seconds                         │',
    '  │  Notifications:   On failure                         │',
  ];

  if (auth) {
    lines.push('  │                                                      │');
    lines.push('  │  Custom Headers:                                     │');
    lines.push('  │    Authorization: ' + auth.substring(0, 35).padEnd(35) + '│');
  }

  lines.push('  ├──────────────────────────────────────────────────────┤');
  lines.push('  │  Setup Steps:                                        │');
  lines.push('  │  1. Go to https://cron-job.org                       │');
  lines.push('  │  2. Create free account                              │');
  lines.push('  │  3. Click "CREATE CRONJOB"                           │');
  lines.push('  │  4. Paste the URL and cron expression above          │');
  lines.push('  │  5. Set timezone to UTC                              │');
  lines.push('  │  6. Save — done!                                     │');
  lines.push('  └──────────────────────────────────────────────────────┘');
  lines.push('');

  return lines.join('\n');
}

// ─── Keep-Awake Config Generator ──────────────────────────────────

/**
 * Generate keep-awake configurations:
 *  - A /health endpoint code snippet
 *  - A GitHub Action pinging every 14 min
 *  - An UptimeRobot config pinging every 5 min
 *
 * @param {{ url: string, outputDir: string, auth: string|null, framework: string }} options
 * @returns {{ healthSnippet: string, githubPath: string|null, uptimeConfig: string }}
 */
function generateKeepAwake({ url, outputDir, auth = null, framework = 'express' }) {
  // 1. Health endpoint code snippet
  let healthSnippet;
  if (framework === 'express') {
    healthSnippet = [
      '// Add this to your Express app to enable keep-awake pings',
      '// Place BEFORE any auth middleware so it responds without authentication',
      '',
      "app.get('/health', (req, res) => {",
      "  res.status(200).json({",
      "    status: 'ok',",
      "    uptime: process.uptime(),",
      "    timestamp: new Date().toISOString()",
      '  });',
      '});',
    ].join('\n');
  } else if (framework === 'fastify') {
    healthSnippet = [
      '// Add this to your Fastify app to enable keep-awake pings',
      '',
      "fastify.get('/health', async (request, reply) => {",
      '  return {',
      "    status: 'ok',",
      '    uptime: process.uptime(),',
      '    timestamp: new Date().toISOString()',
      '  };',
      '});',
    ].join('\n');
  } else {
    // Generic
    healthSnippet = [
      '// Add a /health endpoint to your server',
      '// It must respond with 200 OK instantly',
      '// Place BEFORE any auth middleware',
      '',
      '// GET /health → { "status": "ok" }',
    ].join('\n');
  }

  // 2. GitHub Action for keep-awake (14 min interval)
  const healthUrl = url.replace(/\/+$/, '') + '/health';

  const githubPath = generateGitHubAction({
    url: healthUrl,
    cron: '*/14 * * * *', // Every 14 min — just under the 15 min sleep threshold
    auth: null,           // Health endpoint should not need auth
    outputDir,
    retries: 2,
    delay: 5,
    method: 'GET',
    body: null,
  });

  // 3. UptimeRobot config for keep-awake
  const uptimeConfig = generateUptimeRobotConfig({
    url: healthUrl,
    intervalMinutes: 5,
    auth: null,
  });

  return { healthSnippet, githubPath, uptimeConfig };
}

